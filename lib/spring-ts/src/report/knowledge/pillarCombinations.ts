const seedProfileByIndex = new Map<number, DayPillarProfile>(
  SEED_DAY_PILLAR_PROFILES.map((profile) => [profile.ganziIndex, profile]),
);

export const DAY_PILLAR_PROFILES: readonly DayPillarProfile[] = Array.from({ length: 60 }, (_, index) => {
  const seeded = seedProfileByIndex.get(index);
  if (seeded) return seeded;
  return buildFallbackProfile(index);
});

export const DAY_PILLAR_PROFILE_MAP: ReadonlyMap<string, DayPillarProfile> = new Map(
  DAY_PILLAR_PROFILES.map((profile) => [`${profile.stemCode}-${profile.branchCode}`, profile]),
);

function deriveGanziIndex(stemCode: StemCode, branchCode: BranchCode): number {
  const stemIdx = STEM_ORDER.indexOf(stemCode);
  const branchIdx = BRANCH_ORDER.indexOf(branchCode);
  if (stemIdx < 0 || branchIdx < 0) return 0;
  for (let i = 0; i < 60; i += 1) {
    if (i % 10 === stemIdx && i % 12 === branchIdx) {
      return i;
    }
  }
  return 0;
}

export function getDayPillarProfileByIndex(ganziIndex: number): DayPillarProfile {
  const normalized = ((Math.trunc(ganziIndex) % 60) + 60) % 60;
  return DAY_PILLAR_PROFILES[normalized];
}

export function getDayPillarProfile(stemCode: StemCode, branchCode: BranchCode): DayPillarProfile {
  const key = `${stemCode}-${branchCode}`;
  const direct = DAY_PILLAR_PROFILE_MAP.get(key);
  if (direct) return direct;
  const derivedIndex = deriveGanziIndex(stemCode, branchCode);
  return getDayPillarProfileByIndex(derivedIndex);
}
