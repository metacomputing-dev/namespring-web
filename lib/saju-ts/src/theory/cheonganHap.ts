/**
 * 두 천간 인덱스가 충(沖) 관계인지 판별한다.
 *
 * 천간충은 동일 음양의 상극(相剋) 관계로, 인덱스 차이가 6인 쌍이다.
 * 단, 戊(4)·己(5)는 충 상대가 없으므로 항상 false를 반환한다.
 *
 * 수학적 판별: |stemA - stemB| mod 10 === 6이고,
 * 양자가 모두 양간이거나 모두 음간인 경우 (즉, stemA % 2 === stemB % 2).
 *
 * @param stemA - 첫 번째 천간 인덱스 (0~9)
 * @param stemB - 두 번째 천간 인덱스 (0~9)
 * @returns 충 관계이면 true
 *
 * @example
 * isChung(0, 6)  // true  — 甲庚沖
 * isChung(2, 8)  // true  — 丙壬沖
 * isChung(4, 0)  // false — 戊甲: 극 관계이지만 충은 아님
 * isChung(0, 5)  // false — 甲己合 (합이지 충이 아님)
 */
export function isChung(stemA: StemIdx, stemB: StemIdx): boolean {
  const a = ((stemA % 10) + 10) % 10;
  const b = ((stemB % 10) + 10) % 10;
  // 戊(4)·己(5)는 충 상대가 없다
  if (a === 4 || a === 5 || b === 4 || b === 5) return false;
  // 충 쌍: 0↔6, 1↔7, 2↔8, 3↔9 → 인덱스 차이가 항상 6
  return Math.abs(a - b) === 6;
}

/**
 * 주어진 천간 인덱스의 충(沖) 상대 천간 인덱스를 반환한다.
 *
 * 戊(4)·己(5)는 충 상대가 없으므로 null을 반환한다.
 *
 * @param stemIdx - 천간 인덱스 (0~9)
 * @returns 충 상대 천간 인덱스 (0~9), 없으면 null
 *
 * @example
 * getChungPartner(0)  // 6  — 甲의 충 상대는 庚
 * getChungPartner(6)  // 0  — 庚의 충 상대는 甲
 * getChungPartner(4)  // null — 戊는 충 상대 없음
 */
export function getChungPartner(stemIdx: StemIdx): StemIdx | null {
  const s = ((stemIdx % 10) + 10) % 10;
  if (s === 4 || s === 5) return null;
  return (((s + 6) % 10) as StemIdx);
}

