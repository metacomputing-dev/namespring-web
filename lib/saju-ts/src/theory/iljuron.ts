/**
 * 천간·지지 인덱스로 60갑자 일주(日柱) 데이터를 반환한다.
 *
 * @param stemIdx   - 천간 인덱스 (甲=0 … 癸=9)
 * @param branchIdx - 지지 인덱스 (子=0 … 亥=11)
 * @returns IljuData, 또는 유효하지 않은 조합이면 undefined
 */
export function getIlju(
  stemIdx: StemIdx,
  branchIdx: BranchIdx,
): IljuData | undefined {
  // (stem + branch) % 2 !== 0 이면 60갑자에 없는 조합
  if ((stemIdx + branchIdx) % 2 !== 0) return undefined;
  return ILJU_TABLE.find(
    (e) => e.stem === stemIdx && e.branch === branchIdx,
  );
}

