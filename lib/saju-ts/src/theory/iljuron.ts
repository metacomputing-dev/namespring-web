/**
 * iljuron.ts — 일주론 (日柱論)
 *
 * 사주명리학 60갑자 일주(日柱)별 성격·특성 이론.
 *
 * 일주(日柱)는 태어난 날의 천간(天干)과 지지(地支)로 이루어지며,
 * 명주(命主) 본인의 성격·기질·배우자·직업 성향 등을 나타내는
 * 핵심 기둥이다.
 *
 * 천간 명칭 (한글 로마자 표기)：
 *   GAP(甲)·EUL(乙)·BYEONG(丙)·JEONG(丁)·MU(戊)
 *   GI(己)·GYEONG(庚)·SIN(辛)·IM(壬)·GYE(癸)
 *
 * 지지 명칭 (한글 로마자 표기)：
 *   JA(子)·CHUK(丑)·IN(寅)·MYO(卯)·JIN(辰)·SA(巳)
 *   O(午)·MI(未)·SHIN(申)·YU(酉)·SUL(戌)·HAE(亥)
 */

import type { StemIdx, BranchIdx } from '../core/cycle.js';

// ---------------------------------------------------------------------------
// 1. IljuData 인터페이스
// ---------------------------------------------------------------------------

/**
 * 60갑자 일주(日柱) 데이터.
 */
export interface IljuData {
  /** 일주 번호 (0~59, 甲子=0 … 癸亥=59) */
  index: number;
  /** 천간 인덱스 (甲=0 … 癸=9) */
  stem: StemIdx;
  /** 지지 인덱스 (子=0 … 亥=11) */
  branch: BranchIdx;
  /** 일주 명칭 (한글 로마자) */
  name: string;
  /** 한자 명칭 */
  hanja: string;
  /** 납음오행 명칭 (한글 로마자) */
  nabeum: string;
  /** 납음오행 한자 */
  nabeumHanja: string;
  /**
   * 십이운성(十二運星) — 일간이 일지에서 받는 생왕사절 상태.
   *
   * 십이운성 인덱스: 0=장생, 1=목욕, 2=관대, 3=건록, 4=제왕,
   *   5=쇠, 6=병, 7=사, 8=묘, 9=절, 10=태, 11=양
   */
  sippiUnsung: number;
  /** 십이운성 명칭 (예: '목욕 (沐浴)') */
  sippiUnsungName: string;
  /**
   * 일주에 해당하는 주요 신살(神殺) 목록.
   * 일간·일지 조합에서 자동으로 도출되는 대표 신살.
   */
  sinsal: string[];
  /**
   * 일주 특수 분류 (괴강·일귀·일덕·금여록·음양차착 등).
   * 해당 사항이 없으면 빈 배열.
   */
  teuksu: string[];
  /** 성격 특성 */
  personality: string;
  /** 배우자 특성 */
  spouse: string;
  /** 직업 성향 */
  career: string;
  /** 주의사항 및 특이점 */
  juuiSahang: string;
}

